public class Ex07_01 {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println("[" + i + "] while문을 공부합니다.");

            i++;
            // while 문 사용시 조건에 변화를 줄 수 있는 로직이 반드시 들어가야됨 *
            // 반복할 횟수를 알고 있을때 for 조건만 알고 있을때 while 문
        }
    }
}