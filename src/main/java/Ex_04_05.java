public class Ex_04_05 {
    public static void main(String[] args) {
        int a = 100, b = 200;

        System.out.println(a + " == " + b + " 는 " + "false 이다");
        System.out.println(a + " != " + b + " 는 " + "true 이다");
        System.out.println(a + " > " + b + " 는 " + "false 이다");
        System.out.println(a + " < " + b + " 는 " + "true 이다");
        System.out.println(a + " >= " + b + " 는 " + "false 이다");
        System.out.println(a + " <= " + b + " 는 " + "true 이다");
        System.out.println(a + " = " + b + " 는 " + b + " 이다");
    }
}
